import matplotlib.pyplot as plt
from matplotlib import gridspec

# 方法1：subplot2grid
plt.figure()
ax1 = plt.subplot2grid((3, 3), (0, 0), colspan=3, rowspan=1)
ax1.plot([0, 1], [0, 1])
ax1.set_title("Figure 1")
ax2 = plt.subplot2grid((3, 3), (1, 0), colspan=1, rowspan=1)
ax2.plot([0, 1], [0, 1])
ax2.set_title("Figure 2")
ax3 = plt.subplot2grid((3, 3), (1, 1), colspan=2, rowspan=1)
ax3.plot([0, 1], [0, 1])
ax3.set_title("Figure 3")
ax4 = plt.subplot2grid((3, 3), (2, 0), colspan=1, rowspan=1)
ax4.plot([0, 1], [0, 1])
ax4.set_title("Figure 4")
ax5 = plt.subplot2grid((3, 3), (2, 1), colspan=1, rowspan=1)
ax5.plot([0, 1], [0, 1])
ax5.set_title("Figure 5")
ax6 = plt.subplot2grid((3, 3), (2, 2), colspan=1, rowspan=1)
ax6.plot([0, 1], [0, 1])
ax6.set_title("Figure 6")
plt.tight_layout()
plt.show()
# 方法2：gridspec
plt.figure()
gs = gridspec.GridSpec(3, 3)
ax1 = plt.subplot(gs[0, :])
ax1.plot([0, 1], [0, 1])
ax1.set_title("Figure 1")
ax2 = plt.subplot(gs[1, 0])
ax2.plot([0, 1], [0, 1])
ax2.set_title("Figure 2")
ax3 = plt.subplot(gs[1, 1:])
ax3.plot([0, 1], [0, 1])
ax3.set_title("Figure 3")
ax4 = plt.subplot(gs[2, 0])
ax4.plot([0, 1], [0, 1])
ax4.set_title("Figure 4")
ax5 = plt.subplot(gs[2, 1])
ax5.plot([0, 1], [0, 1])
ax5.set_title("Figure 5")
ax6 = plt.subplot(gs[2, 2])
ax6.plot([0, 1], [0, 1])
ax6.set_title("Figure 6")
plt.tight_layout()
plt.show()
# 方法3：easy to define structure
f, ((ax11, ax12), (ax21, ax22)) = \
    plt.subplots(2, 2, sharex=True, sharey=True)
